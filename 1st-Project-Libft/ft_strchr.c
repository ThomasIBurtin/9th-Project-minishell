#include "libft.h"

int ft_strchr(char *s, char c)
{
	int	i;

	i = 1;
	if (!s[i])
		return (0);
	while (s[i] != '\0')
	{
		if (s[i] == c)
		{
			if(s[i+1] == '\0')
				return(0);
			return (1);
		}
		i++;
	}
	return (0);
}
